CREATE VIEW [cus].v_PROMOTION
             AS
            SELECT
               CAST(NULL AS nvarchar(255)) AS [ITEM_NO],
               CAST(NULL AS nvarchar(255)) AS [LOCATION_NO],
               CAST(NULL AS date) AS [PERIOD_START],
               CAST(NULL AS date) AS [PERIOD_END]
                WHERE 1 = 0;
